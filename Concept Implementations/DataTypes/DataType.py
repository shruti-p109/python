def main():
	# int iObj = 11; - c,c++ etc. - static typing
	iObj = 11 # dynamic typing
	# javascript - typing is optional
	fObj = 3.14
	sObj = "hello world!"
	bObj = False

	print(iObj)
	print(type(iObj))
	print(fObj)
	print(type(fObj))
	print(sObj)
	print(type(sObj))
	print(bObj)
	print(type(bObj))

if __name__ == "__main__":
	main()